    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username=jTextField1.getText();
        String email=jTextField2.getText();
        String password=jTextField3.getText();
        String confirmPassword=jTextField4.getText();
        
        
        if(password.equals(confirmPassword) && password.length() >=4 && password.length() <=20 && confirmPassword.length() >=4 && confirmPassword.length() <=20 )
        {
            jTextField3.setBackground(Color.white);
            jTextField4.setBackground(Color.white);
            jLabel9.setText("");
            try
            {
            File f = new File("sekretnehasla.csv");
            FileWriter fw=new FileWriter(f);
            fw.write(username+";"+email+";"+password);
            fw.close();   
            }
            catch(IOException ex)
            {
            Logger.getLogger(Logowanie.class.getName()).log(level.SEVERE, null, ex);
            }
            
        }
        else
        {
            
            jTextField3.setBackground(Color.red);
            jTextField4.setBackground(Color.red);
            jLabel9.setText("Has≈Ça sƒÖ nie poprawne");
        }
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

